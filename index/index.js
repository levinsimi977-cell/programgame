function startDouble(event)
{
    if(event.currentTarget.id=="doble")
        window.location.href="../doublegame/doublegame.html";
    if(event.currentTarget.id=="light")
        window.location.href="../lightgame/lightgame.html";
    if(event.currentTarget.id=="boolpgia")
        window.location.href="../boolpgia/boolpgia.html";
    if(event.currentTarget.id=="memory")
        window.location.href="../memorygame/memory.html";

}